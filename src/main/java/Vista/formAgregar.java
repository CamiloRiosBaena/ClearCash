    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        List<Clases.MovimientoFinanciero> movimiento = new ArrayList<>();
        Clases.MovimientoFinanciero aux = new Clases.MovimientoFinanciero();
        Clases.CManejoArchivos objetoArchivo = new Clases.CManejoArchivos();
        
        SimpleDateFormat formato = new SimpleDateFormat("dd-MM-yyyy");
        String fecha = formato.format(cldFecha.getDate());
        
        String razon = txtRazon.getText();
        double monto = Double.parseDouble(txtMonto.getText());
        
        aux.setNombreUsuario(NombreUsuario);
        aux.setFecha(fecha);
        aux.setRazon(razon);
        aux.setMonto(monto);
        aux.setTipo( (String) cbxTipo.getSelectedItem());
        
        movimiento.add(aux);
        
        objetoArchivo.setMovimiento(aux);
        
        aux.MostrarInfo(txpPantalla);
        objetoArchivo.registrarInfoUsuario(movimiento);    
    }//GEN-LAST:event_btnAgregarActionPerformed

